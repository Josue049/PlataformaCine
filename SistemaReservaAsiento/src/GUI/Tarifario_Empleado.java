    private void SeleccionarHorario() {
        String horario = "";
        if (this.opcHorario1.isSelected()) {
            horario = "8:00 pm";
        } else if (this.opcHorario2.isSelected()) {
            horario = "6:00 pm";
        } else if (this.opcHorario3.isSelected()) {
            horario = "4:00 pm";
        }
        this.sala.setHorario(horario);
    }
    
