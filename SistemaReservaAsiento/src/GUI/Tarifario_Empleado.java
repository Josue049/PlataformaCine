    private void BotonTarifaEmpleado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonTarifaEmpleado
        double entradaAdulto = ((Number) this.cantidadAdulto.getValue()).doubleValue();
        double entradaInfantil = ((Number) this.cantidadInfantil.getValue()).doubleValue();
        double entradaAdultoMayor = ((Number) this.cantidadAdultoMayor.getValue()).doubleValue();

        double precioAdulto = entradaAdulto * 15;
        double precioInfantil = entradaInfantil * 12;
        double precioAdultoMayor = entradaAdultoMayor * 12;

        double total = precioAdulto + precioInfantil + precioAdultoMayor;
        
        this.SeleccionarHorario();
        
        double var = entradaAdulto + entradaInfantil + entradaAdultoMayor;
        Pago pago = new Pago();
        pago.setMonto(total);
        
        //Ventana Seleccionar Asiento
        this.seleccionarAsiento.iniciar(1,pago,this.pelicula,this.sala, (int) var);//indice empleado:1
        this.setVisible(false);
    }//GEN-LAST:event_BotonTarifaEmpleado

    

